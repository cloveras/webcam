/**
 * lang.php - Multilingual support for webcam gallery
 *
 * Detects visitor language from ?lang=XX query param, cookie, or Accept-Language header.
 * Provides t(string $key) for translated strings and lang_html_attr() for <html lang="">.
 *
 * Supported: en, de, it, fr, nb, nl, es, ja, zh, ko, sv, da, pl, fi, pt, th, tr, id, hi, ms, uk
 */

function detect_lang(): string {
    $supported = ['en','de','it','fr','nb','nl','es','ja','zh','ko','sv','da','pl','fi','pt','th','tr','id','hi','ms','uk'];

    // 1. Query parameter overrides everything and sets a persistent cookie
    if (isset($_GET['lang']) && in_array($_GET['lang'], $supported, true)) {
        $lang = $_GET['lang'];
        setcookie('lang', $lang, time() + 60 * 60 * 24 * 365, '/');
        return $lang;
    }

    // 2. Cookie
    if (isset($_COOKIE['lang']) && in_array($_COOKIE['lang'], $supported, true)) {
        return $_COOKIE['lang'];
    }

    // 3. Accept-Language header
    $header = $_SERVER['HTTP_ACCEPT_LANGUAGE'] ?? '';
    if ($header) {
        preg_match_all('/([a-z]{2})(?:-[a-z]{2})?(?:;q=([0-9.]+))?/i', $header, $matches, PREG_SET_ORDER);
        $best_q = 0.0;
        $best   = 'en';
        foreach ($matches as $m) {
            $tag = strtolower($m[1]);
            $q   = isset($m[2]) && $m[2] !== '' ? (float)$m[2] : 1.0;
            if ($tag === 'nn') $tag = 'nb'; // Norwegian Nynorsk → Bokmål
            if (in_array($tag, $supported, true) && $q > $best_q) {
                $best   = $tag;
                $best_q = $q;
            }
        }
        if ($best !== 'en') return $best;
    }

    return 'en';
}

function lang_html_attr(): string {
    global $LANG;
    $map = [
        'en' => 'en-US', 'de' => 'de-DE', 'it' => 'it-IT', 'fr' => 'fr-FR',
        'nb' => 'nb-NO', 'nl' => 'nl-NL', 'es' => 'es-ES', 'ja' => 'ja-JP',
        'zh' => 'zh-CN', 'ko' => 'ko-KR', 'sv' => 'sv-SE', 'da' => 'da-DK',
        'pl' => 'pl-PL', 'fi' => 'fi-FI', 'pt' => 'pt-PT', 'th' => 'th-TH',
        'tr' => 'tr-TR', 'id' => 'id-ID', 'hi' => 'hi-IN', 'ms' => 'ms-MY',
        'uk' => 'uk-UA',
    ];
    return $map[$LANG] ?? 'en-US';
}

/**
 * Generate <link rel="alternate" hreflang="..."> tags for all supported languages.
 * $base_url: the base URL for this page (no query string), e.g. "https://lilleviklofoten.no/webcam/"
 * English (default) gets no lang param; all others get ?lang=XX.
 */
function lang_hreflang_links(string $base_url): string {
    $langs = [
        'en' => 'en', 'de' => 'de', 'it' => 'it', 'fr' => 'fr',
        'nb' => 'nb', 'nl' => 'nl', 'es' => 'es', 'ja' => 'ja',
        'zh' => 'zh-CN', 'ko' => 'ko', 'sv' => 'sv', 'da' => 'da',
        'pl' => 'pl', 'fi' => 'fi', 'pt' => 'pt', 'th' => 'th',
        'tr' => 'tr', 'id' => 'id', 'hi' => 'hi', 'ms' => 'ms', 'uk' => 'uk',
    ];
    $sep = strpos($base_url, '?') !== false ? '&' : '?';
    $out = '';
    foreach ($langs as $code => $hreflang) {
        $url = ($code === 'en') ? $base_url : $base_url . $sep . 'lang=' . $code;
        $out .= "  <link rel=\"alternate\" hreflang=\"$hreflang\" href=\"$url\">\n";
    }
    $out .= "  <link rel=\"alternate\" hreflang=\"x-default\" href=\"{$base_url}\">\n";
    return $out;
}

function t(string $key): string {
    global $LANG, $TRANSLATIONS;
    $val = $TRANSLATIONS[$LANG][$key] ?? $TRANSLATIONS['en'][$key] ?? $key;
    // Localise lilleviklofoten.no links for non-English languages
    if ($LANG !== 'en' && str_contains($val, 'lilleviklofoten.no')) {
        $ll = 'https://lilleviklofoten.no/' . $LANG;
        $val = str_replace(
            ['href="https://lilleviklofoten.no"', 'href="https://lilleviklofoten.no?', 'href="https://lilleviklofoten.no/webcams/'],
            ['href="' . $ll . '/"',               'href="' . $ll . '/?',              'href="' . $ll . '/webcams/'],
            $val
        );
    }
    return $val;
}

/** Returns "&lang=XX" (for appending to URLs that already have a query string), or "" for English. */
function lang_param(): string {
    global $LANG;
    return $LANG === 'en' ? '' : '&lang=' . $LANG;
}

/** Returns "?lang=XX" (for appending to bare URLs with no query string), or "" for English. */
function lang_query(): string {
    global $LANG;
    return $LANG === 'en' ? '' : '?lang=' . $LANG;
}

/** Returns the translated month name for month 1–12. */
function t_month(int $month): string {
    global $LANG, $TRANSLATIONS;
    $months = $TRANSLATIONS[$LANG]['months'] ?? $TRANSLATIONS['en']['months'];
    return $months[$month - 1] ?? '';
}

/** Returns a localised "Month Year" string, e.g. "gennaio 2026" or "2026年1月". */
function t_month_year(int $month, int $year): string {
    global $LANG;
    if ($LANG === 'ja' || $LANG === 'zh') return "{$year}年{$month}月";
    if ($LANG === 'ko') return "{$year}년 {$month}월";
    return t_month($month) . ' ' . $year;
}

/** Returns a localised "Month Day" string for navigation labels, e.g. "8 marzo" or "8. März". */
function t_month_day(int $month, int $day): string {
    global $LANG;
    $name = t_month($month);
    return match($LANG) {
        'ja', 'zh' => "{$month}月{$day}日",
        'ko'       => "{$month}월 {$day}일",
        'de', 'nb', 'da', 'fi' => "{$day}. {$name}",
        'es', 'pt' => "{$day} de {$name}",
        default    => "{$day} {$name}",
    };
}

/**
 * Returns an inline <select> language selector for the nav bar.
 * Preserves current query parameters, replacing only lang=.
 */
function lang_selector_html(): string {
    global $LANG;
    $names = [
        'en' => 'English',  'de' => 'Deutsch',    'it' => 'Italiano',  'fr' => 'Français',
        'nb' => 'Norsk',    'nl' => 'Nederlands',  'es' => 'Español',   'ja' => '日本語',
        'zh' => '中文',     'ko' => '한국어',       'sv' => 'Svenska',   'da' => 'Dansk',
        'pl' => 'Polski',   'fi' => 'Suomi',        'pt' => 'Português', 'th' => 'ภาษาไทย',
        'tr' => 'Türkçe',   'id' => 'Indonesia',    'hi' => 'हिन्दी',    'ms' => 'Melayu',
        'uk' => 'Українська',
    ];
    $params = $_GET;
    unset($params['lang']);
    $qs = count($params) ? '?' . http_build_query($params) : null;

    $html = '🌐 <select class="lang-select" onchange="location.href=this.value">';
    foreach ($names as $code => $name) {
        $url      = ($qs ? $qs . '&' : '?') . 'lang=' . $code;
        $selected = $code === $LANG ? ' selected' : '';
        $html    .= '<option value="' . htmlspecialchars($url) . '"' . $selected . '>' . htmlspecialchars($name) . '</option>';
    }
    $html .= '</select>';
    return $html;
}

